for traditionalkey, simplifiedvalue in tsmap.items():
    reverse_tsmap[simplifiedvalue].append(traditionalkey)


def simplify(w):
    return ''.join([tsmap.get(s, s) for s in w])


def is_simplified(w):
    return w == simplify(w)


def is_traditional(w):
    for a in w:
        if a in reverse_tsmap and a not in tsmap:
            return False
    return True